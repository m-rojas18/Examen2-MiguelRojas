    private void jb_crearTecnicoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_crearTecnicoMouseClicked
        //Crear Tecnico

        String nombre = null;
        int edad = 0;
        String genero = null;
        boolean crear = true;

        //Validar Entradas
        //Nombre
        if (tf_nombreTecnico.getText().isEmpty()) {
            JOptionPane.showMessageDialog(jd_menuTecnicos, "Falta ingresar el nombre del tecnico.");
            crear = false;
        } else {
            nombre = tf_nombreTecnico.getText();
        }
        //Edad
        if ((int) js_edad.getValue() < 18) {
            JOptionPane.showMessageDialog(jd_menuTecnicos, "Debe ingresar una edad valida [ > 18]");
            crear = false;
        } else {
            edad = (int) js_edad.getValue();
        }

        //Genero
        if (rb_M.isSelected()) {
            genero = "M";
        } else {
            genero = "F";
        }

        if (crear) {
            //Crear Tecnico
            Tecnico t = new Tecnico(nombre, edad, genero);
            //Agregar a lista
            admin_tecnicos.getLista_tecnicos().add(t);
            admin_tecnicos.escribirArchivo();
            refrescarListasTecnicos();
            JOptionPane.showMessageDialog(jd_menuTecnicos, "Se creo el tecnico exitosamente!!");
            tf_nombreTecnico.setText("");
            js_edad.setValue(0);
            rb_M.setSelected(true);
            rb_F.setSelected(false);
        }

    }//GEN-LAST:event_jb_crearTecnicoMouseClicked

